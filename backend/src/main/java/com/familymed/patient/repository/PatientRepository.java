package com.familymed.patient.repository;

import com.familymed.patient.entity.Patient;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Optional;
import java.util.UUID;

@Repository
public interface PatientRepository extends JpaRepository<Patient, UUID> {
    Optional<Patient> findByPatientCode(String patientCode);
    Optional<Patient> findTopByPatientCodeStartingWithOrderByPatientCodeDesc(String prefix);
    List<Patient> findByDoctorUserId(UUID doctorId);
    List<Patient> findByStatus(Patient.PatientStatus status);
}

